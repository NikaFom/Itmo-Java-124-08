package itmo.java.basics.lab2;

public class Calculator {
    public double sum(double number1, double number2) { // сумма
        double sum = number1 + number2;
        System.out.println(sum);
        return sum;
    }

    public long sum(long number1, long number2) {
        long sum = number1 + number2;
        System.out.println(sum);
        return sum;
    }

    public int sum(int number1, int number2) {
        int sum = number1 + number2;
        System.out.println(sum);
        return sum;
    }

    public double division(double number1, double number2) { // деление
        double division = number1 / number2;
        System.out.println(division);
        return division;
    }

    public double division(long number1, long number2) {
        double division = number1 / number2;
        System.out.println(division);
        return division;
    }

    public double division(int number1, int number2) {
        double division = number1 / number2;
        System.out.println(division);
        return division;
    }

    public double multiplication(double number1, double number2) { // умножение
        double multiplication = number1 * number2;
        System.out.println(multiplication);
        return multiplication;
    }

    public long multiplication(long number1, long number2) {
        long multiplication = number1 * number2;
        System.out.println(multiplication);
        return multiplication;
    }

    public int multiplication(int number1, int number2) {
        int multiplication = number1 * number2;
        System.out.println(multiplication);
        return multiplication;
    }

    public double subtraction(double number1, double number2) { // вычитание
        double subtraction = number1 - number2;
        System.out.println(subtraction);
        return subtraction;
    }

    public long subtraction(long number1, long number2) {
        long subtraction = number1 - number2;
        System.out.println(subtraction);
        return subtraction;
    }

    public int subtraction(int number1, int number2) {
        int subtraction = number1 - number2;
        System.out.println(subtraction);
        return subtraction;
    }
}